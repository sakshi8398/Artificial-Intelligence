% Assuming if city x has path to y then city y has also path to x with
% same cost of travel below is function to represent the same.

connected(X,Y,L):-edge(X,Y,L);edge(Y,X,L).


% solve function would be called by the user to find the path between the node and goal and
% solve function will internally calls the depth_first_search.
% it will return path and the cost to travel to node to goal node.
% reverse function is called to just reverse the list of city which are travel and return the list of city from node to goal

solve(Node,Goal,Solution1,Cost):-
      depth_first_search(Node,Goal,[Node],Solution,Cost),
      reverse(Solution,Solution1).
      %write("path is : "),write(Solution),nl,
      %write("Cost is : "),write(Cost),nl.

% simplest search if the cities are directly connected then we simple return the path and the cost of city

depth_first_search(Node,Goal,Path,[Goal|Path],Cost):-
       connected(Node,Goal,Cost).


% if the cities are not connected directly then we need to find the path through some intermediate nodes
% I have call connected to search for the cities which are directly or
% indirectly connected to the given node after that to avoid the cycle
% that we do not consider the same city again and again I am calling
% member function to check if i have included the city in list already
% of not then call th depth_first_search recursively to find the goal
% node. and computing its cost to travle there.

depth_first_search(Node,Goal,Visited,Path,Cost):-
       connected(Node,Node1,C),
       Node1\=Goal,
       \+member(Node1,Visited),
       depth_first_search(Node1,Goal,[Node1|Visited],Path,Cost1),
       Cost is C+Cost1.


